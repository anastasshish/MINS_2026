from sos.errors import ОшибкаТипов

БАЗОВЫЕ_ТИПЫ = {
    "целое",
    "вещественное",
    "строка",
    "символ",
    "логическое",
    "пустое",
}


def это_базовый_тип(имя: str) -> bool:
    return имя in БАЗОВЫЕ_ТИПЫ


def проверить_совместимость(ожидаемый: str, фактический, классы: dict | None = None) -> bool:
    if ожидаемый == "пустое":
        return getattr(фактический, "имя_типа", None) == "пустое" or getattr(фактический, "значение", object()) is None
    if getattr(фактический, "имя_типа", None) == "пустое" and getattr(фактический, "значение", object()) is None:
        if ожидаемый == "пустое":
            return True
        if not это_базовый_тип(ожидаемый) and not ожидаемый.endswith("[]"):
            return True
        return False
    if ожидаемый.endswith("[]"):
        from sos.values import Массив
        if isinstance(фактический, Массив):
            if фактический.имя_типа == ожидаемый:
                return True
            base = ожидаемый[:-2]
            return фактический.тип_элемента == base
        return False
    if ожидаемый.startswith("словарь"):
        from sos.values import Словарь
        return isinstance(фактический, Словарь)
    if not это_базовый_тип(ожидаемый):
        from sos.values import ЭкземплярКласса
        if isinstance(фактический, ЭкземплярКласса):
            return фактический.класс.имя == ожидаемый or фактический.класс.наследует(ожидаемый)
        return getattr(фактический, "имя_типа", None) == ожидаемый
    return getattr(фактический, "имя_типа", None) == ожидаемый


def проверить_элемент(тип_элемента: str, значение) -> bool:
    return проверить_совместимость(тип_элемента, значение)


def привести_значение(целевой_тип: str, значение):
    from sos.values import Значение, ЭкземплярКласса, Массив, Словарь

    if целевой_тип.endswith("[]"):
        if isinstance(значение, Массив):
            return значение
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к {целевой_тип}")

    if целевой_тип.startswith("словарь"):
        if isinstance(значение, Словарь):
            return значение
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к {целевой_тип}")

    if not это_базовый_тип(целевой_тип):
        if getattr(значение, "класс", None) is not None:
            if значение.класс.имя == целевой_тип or значение.класс.наследует(целевой_тип):
                return значение
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к {целевой_тип}")

    v = значение.значение
    if v is None:
        if целевой_тип == "пустое":
            return Значение("пустое", None)
        raise ОшибкаТипов(f"невозможно привести пустое к {целевой_тип}")

    if целевой_тип == "целое":
        if isinstance(v, bool):
            return Значение("целое", int(v))
        if isinstance(v, (int, float)):
            return Значение("целое", int(v))
        if isinstance(v, str):
            s = v.strip()
            if not s:
                raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к целое")
            try:
                return Значение("целое", int(s))
            except ValueError:
                raise ОшибкаТипов(f"невозможно привести строку \"{v}\" к целое")
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к целое")

    if целевой_тип == "вещественное":
        if isinstance(v, (int, float)):
            return Значение("вещественное", float(v))
        if isinstance(v, str):
            s = v.strip()
            if not s:
                raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к вещественное")
            try:
                return Значение("вещественное", float(s))
            except ValueError:
                raise ОшибкаТипов(f"невозможно привести строку \"{v}\" к вещественное")
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к вещественное")

    if целевой_тип == "строка":
        return Значение("строка", str(v))

    if целевой_тип == "символ":
        if isinstance(v, str) and len(v) == 1:
            return Значение("символ", v)
        if isinstance(v, int):
            return Значение("символ", chr(v))
        raise ОшибкаТипов(f"невозможно привести {значение.имя_типа} к символ")

    if целевой_тип == "логическое":
        return Значение("логическое", bool(v))

    if целевой_тип == "пустое":
        return Значение("пустое", None)

    raise ОшибкаТипов(f"неизвестный тип {целевой_тип}")
